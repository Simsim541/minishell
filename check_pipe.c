#include "minishell.h"

int nbr_pipes(char *str)
{
    int i;
    int n;

    n = 0;
    i = 0;
    if (!str[i])
        return (0);
    while (str[i])
    {
        if (str[i] == '|')
            n++;
        i++;
    }
}